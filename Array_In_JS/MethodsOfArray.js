//Push

let girlName = ["Reshma","Alina","Hima"];

girlName.push("Sabitra");

console.log(girlName);


// POP
girlName.pop();
console.log(girlName);

//Shift
girlName.shift();
console.log(girlName);

//unshift

girlName.unshift("Sabitra");
console.log(girlName);

//filter

girlName.filter(gn=> gn==="Reshma");
console.log(girlName);
